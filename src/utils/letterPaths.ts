/**
 * Get the SVG path for a letter in a specific language
 * @param letter - The letter to get the path for
 * @param language - The language (English, Tamil, or Sinhala)
 * @returns SVG path string
 */
export function getLetterPath(letter: string, language: Language): string {
  const languagePaths = LETTER_PATHS[language];
  if (!languagePaths) {
    console.warn(`No paths found for language: ${language}`);
    return '';
  }

  const path = languagePaths[letter];
  if (!path) {
    console.warn(`No path found for letter: ${letter} in language: ${language}`);
    // Return a simple default path (circle)
    return 'M 100 50 Q 150 50 150 100 Q 150 150 100 150 Q 50 150 50 100 Q 50 50 100 50';
  }

  return path;
}

/**
 * Check if a letter exists for a language
 */
export function hasLetter(letter: string, language: Language): boolean {
  const languagePaths = LETTER_PATHS[language];
  return languagePaths ? letter in languagePaths : false;
}

/**
 * Get all available letters for a language
 */
export function getLettersForLanguage(language: Language): string[] {
  const languagePaths = LETTER_PATHS[language];
  return languagePaths ? Object.keys(languagePaths) : [];
}

